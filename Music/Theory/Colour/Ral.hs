ral_colour_tbl :: [(Int, String, Srgb.Colour Double)]
ral_colour_tbl =
  let f (k, l, a, b, nm) = (k, nm, Cie.cieLAB Illuminant.d65 l a b)
  in map f ral_colour_data

{- | Ral lookup

